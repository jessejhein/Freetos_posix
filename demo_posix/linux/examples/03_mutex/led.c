/************************************************************************************************
 * File: 			led.c
 * Description:		control LED for module board(non-POSIX compliant)
 ***********************************************************************************************/

unsigned int led_state[3];
#include <stdio.h>

/************************************************************************************************
 * Turn LED On
 ************************************************************************************************/
void led_on(unsigned short led){
	led_state[led] = 1; 
	printf("LED %d is ON.\n", (int)led);
}

/************************************************************************************************
 * Turn LED Off
 ************************************************************************************************/
void led_off( unsigned short led){
	led_state[led] = 0; 
	printf("LED %d is OFF.\n", (int)led);
}

/************************************************************************************************
 * Current status of LED
 ************************************************************************************************/
int led_status(unsigned short led){
	switch(led){
		case 0:	case 1:	case 2: 
			return led_state[led];					
		default:
			return -1;
	}	
}

/************************************************************************************************
 * Initialize LED-pins for Digital Outputs
 ************************************************************************************************/
void led_init( void ){
	led_state[0] = 0;
	led_state[1] = 0;
	led_state[2] = 0;
	printf("ALL LEDs initialised successfully.\n");
}
