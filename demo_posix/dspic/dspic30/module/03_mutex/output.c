#include <define.h>
#include <unistd.h>
#include <fcntl.h>

/************************************************************************************************
 * Open the file handler
 ************************************************************************************************/
int screen_open(){
	return open(UARTA, O_RDWR); 
}

/************************************************************************************************
 * Write to file
 ************************************************************************************************/
int screen_out(int fd, char* buf, int byte){
	return write(fd, buf, byte);;
}
